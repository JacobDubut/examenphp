<h3>Un moyen facile de gérer votre intolérance</h3>

<div class="content">
    <!-- pourquoi -->
    <article class="pourquoi">
        <span id="box-pourquoi" class="box-img"></span>
        <h4>Pourquoi ?</h4>
        <p>De nos jours, il y a de plus en plus de personnes qui souffrent d’intolérances alimentaires. La plupart d’entre elles, ne le savent pas et/ou ne se soignent pas. Nous vous proposons une solution pour diminuer votre intolérance au gluten basée sur le long terme. En combinant le suivi d’un médecin et la méthode du  journal du bord, vous allez pouvoir gérer quotidiennement votre consommation d’aliments contenant du gluten.</p>
    </article>
    <!-- comment -->
    <article class="comment">
        <span id="box-comment" class="box-img"></span>
        <h4>Comment ?</h4>
        <p>Le journal de bord est une méthode basée sur la prise de note quotidienne de vos habitudes alimentaires. GlutenFree, vous permet de poster ces notes en ligne, sous forme d’un fil d’actualité commenté. Mais aussi de rester en contact avec votre médecin, et ainsi lui permettre de réorienter votre suivi/traitement rapidement en cas de problème digestif. L’application  vous fait donc  gagner du temps, et accélère la diminution de votre intolérance au gluten.</p>
    </article>
